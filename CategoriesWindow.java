    private void jazaMasomo(TreeSet masomo) {
        int i = 0;
        DefaultTableModel model = (DefaultTableModel)jTableMasomo.getModel();
        model.setRowCount(i);
       
        TreeMap masomoYote = Settings.loadAllSubjects();
        
        for(Object somo : masomo){
            Object[] data = new Object[3];
            data[0] = 1+i;
            data[1] = somo;
            data[2] = masomoYote.get(somo);
            model.setRowCount(i++);
            model.addRow(data);
        }
        
        for(Object obj : Settings.loadAllSubjects().keySet()){
            if(!masomo.contains(obj)){
                jComboBoxMasomo.addItem(obj.toString());
            }
        }
    }  
