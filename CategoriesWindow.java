    void jazaTableMasomo(String[] premNos) {
        this.premNos = premNos;
        TreeSet masomo;
        
        if(premNos.length == 1){
            Mwanafunzi mwfnz = shule.getMwanafunzi(premNos[0]);
            masomo = mwfnz.getMasomo();
        }
        else{
            masomo = shule.getMasomo();
        }
        jazaMasomo(masomo);
    }
    
    private TreeSet getMasomo() {
        TreeSet masomo = new TreeSet();
        
        int rows = jTableMasomo.getRowCount();
        for(int i = 0; i < rows; i++){
            masomo.add(jTableMasomo.getValueAt(i, 1));
        }
        
        return masomo;
    }
    
    private void deleteMasomo(){
        TreeSet masomo = getMasomo();
       
        int[] selected = jTableMasomo.getSelectedRows();
        for(int i = 0; i < selected.length; i++){
            masomo.remove(jTableMasomo.getValueAt(selected[i], 1));
        }
        jazaMasomo(masomo);
    }
    
    
    
